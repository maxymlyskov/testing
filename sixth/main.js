const n = 100;
// Перебираємо всі числа від 1 до 100
for (let i = 1; i <= n; i++) {
  // Створюємо ще один перебор для порівняння чисел
  for (let j = 2; j <= i; j++) {
    // Ділимо ці числа з залишком та перевіряємо чи залишок дорівнює нулю та два числа рівні,
    //  якщо два числа рівні, то додаємо до перебору, якщо число з другого перебору менше - відсіюємо ці числа
    if (i % j === 0 && j < i) {
      break;
    } else if (j === i) {
      console.log(i);
    }
  }
}
